option_template = "  `{option_number}.` _{option_text}_\n"


def questionize_single(question):
	"Gets a question and combines it with its options and also gives it markdown"
	options_text = ""
	for i, option in enumerate(question['options']):
		options_text += option_template.format(option_number=i+1, option_text=option)

	return question_template.format(question_text=question['text'], options_text=options_text)


def questionize_single_answerer(question):
	"Gets a question and combines it with its options and also gives it markdown"
	options_text = ""
	for i, option in enumerate(question['options']):
		options_text += option_template.format(option_number=i+1, option_text=option)

	return question_template.format(question_text=question['text-answerer'], options_text=options_text)


def questionize_bulk(questions):
	"Gets all questions (dict) and returns questionized one"
	questions = dict(questions)
	for i in questions:
		questions[i]['text'] = questionize_single(questions[i])
		questions[i]['text-answerer'] = questionize_single_answerer(questions[i])
		# questions[i]['text-anwerer'] = questionize_single_answerer(questions[i])
	return questions


